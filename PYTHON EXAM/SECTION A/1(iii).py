first_number = int(input("enter the first number: "))
second_number = int(input("enter the second number "))

sum = second_number + first_number
print(f"the sum of the first number and second number is: {sum}")

difference = second_number - first_number
print(f"the difference of the second number and the first number is: {difference}")

product = second_number * first_number
print(f"the product of the second number and the first number is: {product}")

quotient = second_number / first_number
print(f"the quotient of the second number and the first number is: {quotient}")